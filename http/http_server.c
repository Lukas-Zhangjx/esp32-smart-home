/**
 * @file    http_server.c
 * @brief   HTTP server module implementation
 *
 * Uses the ESP-IDF esp_http_server component.
 * The static page (index.html) is embedded into the firmware via CMakeLists
 * EMBED_FILES and read directly from flash at runtime — no filesystem required.
 *
 * Sensor data is read by the DHT11 driver; the relay control stub functions
 * will be replaced once the GPIO driver is complete.
 */

#include <string.h>
#include "esp_err.h"
#include "esp_log.h"
#include "esp_http_server.h"
#include "http_server.h"
#include "dht11.h"
#include "light_ctrl.h"

static const char *TAG = "http_server";

/* ----------------------------------------------------------------
 * Embedded static resources (generated by the CMakeLists EMBED_FILES directive)
 * Symbol naming rule: ESP-IDF uses only the filename part (no directory),
 * replacing non-alphanumeric characters with _
 * web/index.html -> index.html -> _binary_index_html_start
 * ---------------------------------------------------------------- */
extern const uint8_t index_html_start[] asm("_binary_index_html_start");
extern const uint8_t index_html_end[]   asm("_binary_index_html_end");

/* Server handle, used to release resources on stop */
static httpd_handle_t s_server = NULL;


/* ================================================================
 *  Sensor data cache — updated by dht11_read, read directly by HTTP handlers.
 *  Avoids triggering a DHT11 read on every HTTP request
 *  (DHT11 sampling period >= 2 s).
 * ================================================================ */

/* Cache the most recently successful temperature and humidity reading */
static dht11_data_t s_sensor_cache = {
    .temperature = 0.0f,
    .humidity    = 0.0f,
};

/* Cache obstacle detection state: 1 = detected (door closed), 0 = no obstacle (door open) */
static int s_obstacle_cache = 0;

/* Cache IR sensor state: 1 = motion detected, 0 = no motion */
static int s_ir_cache = 0;

/* Cache light sensor: percent 0-100, raw ADC value, digital state 1=bright/0=dark */
static int s_light_percent = 0;
static int s_light_raw     = 0;
static int s_light_digital = 0;

/**
 * @brief  Update the sensor cache; should be called periodically from main_task
 *         (recommended interval >= 2 s).
 *
 * On read failure the previous valid data is retained — the cache is not cleared.
 */
void http_server_update_sensor(void)
{
    dht11_data_t data;
    esp_err_t ret = dht11_read(&data);
    if (ret == ESP_OK) {
        s_sensor_cache = data; /* update cache */
    } /* on read failure retain the last cache; no log printed (DHT11 module pending replacement) */
}

void http_server_update_obstacle(int detected)
{
    s_obstacle_cache = detected;
}

void http_server_update_ir(int detected)
{
    s_ir_cache = detected;
}

void http_server_update_light(int percent, int raw, int digital)
{
    s_light_percent = percent;
    s_light_raw     = raw;
    s_light_digital = digital;
}

